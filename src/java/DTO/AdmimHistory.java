package DTO;

/**
 *
 * @author handez
 */
public class AdmimHistory {
    private String id;
    private String orderid;
    private String date;
    private double total ;

    public AdmimHistory() {
    }

    public AdmimHistory(String id, String orderid, String date, double total) {
        this.id = id;
        this.orderid = orderid;
        this.date = date;
        this.total = total;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getOrderid() {
        return orderid;
    }

    public void setOrderid(String orderid) {
        this.orderid = orderid;
    }

    

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
    
}
